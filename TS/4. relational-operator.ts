// Equal to
console.log("--Equal to--\n");
console.log("1 == 1 :", 1 == 1); // true
console.log("1 == 2 :", 1 == 2); // false
console.log("1 == '1' :", 1 == "1"); // true

// Strict equal to
console.log("\n--Strict equal to--\n");
console.log("1 === 1 :", 1 === 1); // true
console.log("1 === 2 :", 1 === 2); // false
console.log("1 === '1' :", 1 === "1"); // false

// Not equal to
console.log("\n--Not equal to--\n");
console.log("1 != 1 :", 1 != 1); // false
console.log("1 != 2 :", 1 != 2); // true
console.log("1 != '1' :", 1 != "1"); // false

// Strict not equal to
console.log("\n--Strict not equal to--\n");
console.log("1 !== 1 :", 1 !== 1); // false
console.log("1 !== 2 :", 1 !== 2); // true
console.log("1 !== '1' :", 1 !== "1"); // true

// Greater than or equal to
console.log("\n--Greater than or equal to--\n");
console.log("1 > 1 :", 1 > 1); // false
console.log("1 > 2 :", 1 > 2); // false
console.log("2 > 1 :", 2 > 1); // true
console.log("1 >= 1 :", 1 >= 1); // true

// Less than or equal to
console.log("\n--Less than or equal to--\n");
console.log("1 < 1 :", 1 < 1); // false
console.log("1 < 2 :", 1 < 2); // true
console.log("2 < 1 :", 2 < 1); // false
console.log("1 <= 1 :", 1 <= 1); // true
