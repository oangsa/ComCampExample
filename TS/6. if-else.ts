// if - else

console.log("--if - else--\n");

let grade: number = 80;

if (grade >= 80) {
    console.log("A");
}
else if (grade >= 70) {
    console.log("B");
}
else if (grade >= 60) {
    console.log("C");
}
else {
    console.log("F");
}


