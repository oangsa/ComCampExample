// String
console.log("\n--String--\n");

let str: string = "Hello, World!";
let str2: string = 'Hello, World!';
let str3: string = `Hello, World!`;

// string interpolation
let camp: string = "ComCamp36";
let str4: string = `Welcome to ${camp}`;

console.log(str);
console.log(str2);
console.log(str3);
console.log(str4);



// Boolean
console.log("\n--Boolean--\n");

let isTrue: boolean = true;
let isFalse: boolean = false;

console.log(isTrue);
console.log(isFalse);



// Undefined
console.log("\n--Undefined--\n");

let u: undefined = undefined;
let uu: undefined;

console.log(u);
console.log(uu);


